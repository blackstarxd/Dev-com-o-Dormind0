#Pode ser feito utilizando o int no lugar do float. (Só que assim o número deixaria de ser real.)
primeiro_numero = float(input("Digite o primeiro número: "))
segundo_numero = float(input("Digite o segundo número: "))

resultado_subtracao = primeiro_numero - segundo_numero #o calculo da subtração é armazenado na variavel resultado_subtracao.

print("O resultado da subtração é: " +str(resultado_subtracao)) #exibe o resultado.